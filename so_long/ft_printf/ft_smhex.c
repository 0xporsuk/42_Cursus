#include "ft_printf.h"

int	ft_smhex(unsigned int x)
{
	int	i;

	i = 0;
	if (x >= 16)
		i += ft_smhex(x / 16);
	if ((x % 16) < 10)
		i += ft_putchar((x % 16) + '0');
	else
		i += ft_putchar((x % 16) + 87);
	return (i);
}
